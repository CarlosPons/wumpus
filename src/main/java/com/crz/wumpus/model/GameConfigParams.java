package com.crz.wumpus.model;

public class GameConfigParams {
	
	private int boardSize;
	private int pitAmount;
	private int arrowAmount;
	
	public int getBoardSize() {
		return boardSize;
	}
	public void setBoardSize(int boardSize) {
		this.boardSize = boardSize;
	}
	public int getPitAmount() {
		return pitAmount;
	}
	public void setPitAmount(int pitAmount) {
		this.pitAmount = pitAmount;
	}
	public int getArrowAmount() {
		return arrowAmount;
	}
	public void setArrowAmount(int arrowAmount) {
		this.arrowAmount = arrowAmount;
	}
	
	
}
